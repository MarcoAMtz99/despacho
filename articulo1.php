          <h2 style="text-align:left;">¿CUÁL ES LA VENTAJA DE UN SEGURO DE AUTO?</h2>
          <span style="text-align:right; color: gray;">Por: David Moreno. Coopertura, Agente de Seguros y de Fianzas, Sa de Cv.</span>
          <p>Esta es una pregunta constante en los conductores al considerar la contratación de un aparente “costoso” seguro de auto, así como la desconfianza en el pago real ante un percance vial.
              Esas dudas y desconfianza usualmente se deben al desconocimiento del funcionamiento de la póliza de Auto y de los servicios que están presentes en la póliza, los cuales están diseñados para responder ante sucesos imprevistos que muchas veces representan un desfalco financiero repentino y la dificultad de mantener la estabilidad económica personal.

              Primero que nada, debemos ver a la póliza de autos como un paquete de servicios de diferente índole unidos en la póliza, ya que amplían la cobertura tanto al auto como al conductor u ocupantes, sin embargo, ¿Cuáles y en qué consisten esas coberturas?

              Un claro ejemplo para responder estas preguntas son las:
              </p>
             <h3><strong>COBERTURAS PRINCIPALES</strong></h3> 
             <h4>*Responsabilidad Civil Bienes y Personas</h4>
             <p>En caso de que el asegurado resulte responsable por los daños causados accidentalmente a terceras personas o bienes, la aseguradora cubre, hasta el límite de la suma asegurada, los daños causados.
            El Art. 46 del reglamento de tránsito de la CDMX indica que cada vehículo deberá contar con una póliza de Responsabilidad Civil para circular en la Ciudad de México, así como el Art. 74 Bis de la Ley de Caminos, Puentes y Autotransporte Federal para poder circular en las carreteras del país.
            El no contar con este tipo de pólizas puede ser acreedor a una multa.
            </p>

            <h4>*Asistencia Vial:</h4>
            <p>Ayuda cuando el automóvil llegue a presentar algún mal funcionamiento como:</p>
           <p>Batería baja
Falta de Gasolina
Falla en algún fusible
Pinchadura de neumático, ayuda para cambiarlo.
Grúas para traslado del automóvil a casa o taller en caso de desperfecto, no solamente por colisión.
Cobertura Nacional.
<br>
La aseguradora envía personal calificado para asistir al conductor en esas situaciones.
</p>

 <h4>*Asistencia Jurídica</h4><p>
Asesoría y gestión jurídica y en muchos casos, dependiendo de la aseguradora, 
Otorgamiento de fianzas o causiones, con el fin de garantizar la libertad inmediata en caso de un procedimiento ante el Ministerio Público o Juzgado Penal a consecuencia de un percance vial.
</p>
<h4>*Gastos Médicos a ocupantes</h4><p> 
En caso de que los ocupantes del automóvil asegurado requieran atención médica inmediata y tratamientos médicos después del accidente, la aseguradora realiza el pago total de los gastos incurridos hasta el límite de la suma asegurada.

Generalmente se cubren los siguientes gastos:

Hospitalización, medicamentos y tratamientos.
Honorarios de la atención Médica
Aparatos y Prótesis
Enfermeros
Servicio de Ambulancia 
</p>
<h4>*Robo total</h4> <p> 
Si el Automóvil asegurado fuera robado y no se pudiera recuperar en el periodo ordinario que marca la ley, 30 días naturales, la aseguradora indemnizará a la persona que presente la documentación correspondiente la suma asegurada con la cual se formalizó el contrato, ya sea, a valor factura, para autos nuevos y de hasta dos años de antigüedad, o a valor comercial, el cual se va actualizando cada mes, o bien, a valor convenido, que generalmente se utiliza para garantizar préstamos o para autos clásicos, en el cual, un perito valuador determinaría el valor del auto, por ser un clásico, y a ese valor se realizaría el pago indemnizatorio.

Estos tres tipos de valores asegurables se especifican en la póliza de seguro desde el momento de la contratación y así es como quedará estipulado durante la vigencia del contrato.
</p>
<h4>*Cobertura Amplia. Daños Materiales</h4> <p> 
En síntesis, es la cobertura que incluye a todas las anteriores, además de incluir los siguientes riesgos: 

Colisión, Vuelcos, incendio, rayo, explosión, Rotura de Cristales, Actos violentos que dañen al auto si es realizado por personas que participan en algún paro, manifestación, alborotos populares, motines, o bien, daños causados por las autoridades que intervengan en el control de este tipo de actos.
</p>
 <strong><h3>COBERTURAS ADICIONALES</h3> </strong>
<p>Este tipo de Coberturas se solicitan de manera individualizada para cada tipo de automóvil y por solicitud expresa del asegurado.</p>
        
 <h4>*Equipo especial </h4> <p> 
Son aquellas modificaciones que se integran al vehículo asegurado y lo hacen diferente de los demás de su misma serie de producción; es decir, incluyen: Pantallas, equipo de sonido, rines, llantas de medidas diferentes a las del diseño original del vehículo y cualquier otro aditamento que se le agregue al auto. Se dividen en dos tipos de riesgos:
a) Daños materiales al equipo reportado como especial
b) Robo total del equipo agregado al vehículo como consecuencia del robo total del vehículo.
</p>
 <h4>*Extensión de Responsabilidad Civil </h4> <p> 
Algunas aseguradoras ofrecen este servicio y consiste en ampliar la cobertura y suma asegurada al contratante cuando él sea el conductor de otro vehículo, aunque no sea el que aseguró inicialmente, siempre y cuando esté conduciendo un vehículo de condiciones similares: Tipo, uso y servicio.
 
Nota: No aplica para cuando el contratante esté conduciendo un auto de renta por no ser del mismo uso.
</p>
 <h4>*Responsabilidad Civil en exceso </h4> <p>
Cobertura que ampara gastos en un exceso de la cantidad contratada en la cobertura de “Responsabilidad Civil bienes y Personas” que sean necesarios adicionales para cubrir los daños causados a terceros.
</p>
 <h4>*Adaptaciones y Conversiones </h4> <p>
Todas las modificaciones o adaptaciones hechas al mecanismo o carrocería del vehículo asegurado que no están incluidas en la factura del vehículo y que se requieren para el funcionamiento al que se le destina se le consideran como modificación o adaptación. Esta cobertura ofrece una cantidad de dinero para amparar cualquier daño o robo total de las adaptaciones o conversiones realizadas y se dividen en dos riesgos.
a)  Daños materiales a las adaptaciones a consecuencia de cualquiera de los riesgos amparados en la cobertura de daños materiales.
b)  Robo total de las adaptaciones a consecuencia del robo total del vehículo.
 </p>
<h4>*Excensión de deducibles por pérdida total o parcial</h4> <p> 
Si en la póliza tienen contratada la cobertura Amplia, en caso de siniestro, el asegurado no pagará el deducible contemplado para daños materiales, sean totales o parciales.
</p>



*Auto sustituto
La aseguradora proporciona un auto en calidad de reemplazo temporal para que el asegurado pueda desplazarse en los días en los que se esté reparando el vehículo asegurado. El plazo de este auto sustituto se estipula desde el inicio del seguro. 

*Muerte Accidental del conductor o pérdida de miembros
Cobertura que ampara una cantidad de dinero para indemnizar la pérdida de algún miembro o la vida del conductor debido a un accidente automovilístico.
Si el conductor perdiera algún órgano se le indemnizará al conductor afectado; y si perdiera la vida, dentro de un plazo de 90 días posteriores a la fecha del siniestro, la aseguradora indemnizará a los beneficiarios que se hayan designado en la póliza, el pago de las cantidades destinadas en la suma asegurada destinada para estos riesgos.

